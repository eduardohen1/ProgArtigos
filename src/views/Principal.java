    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try{
            Artigos artigos = new Artigos();
            artigos.setRepositorio(txtRepositorio.getText());
            artigos.setArtigo(txtTitulo.getText());
            artigos.setAutores(txtAutores.getText());
            artigos.setAno(Integer.parseInt(txtAno.getText()));
            artigos.setPalavrasChaves(txtPalavrasChave.getText());
            artigos.setDoi(txtDoi.getText());
            artigos.setIntroducao(txtIntroducao.getText());
            artigos.setConclusao(txtConclusao.getText());
            artigos.setIntroducao_pt(Transllation.traduzir(txtIntroducao.getText()));
            artigos.setConclusao_pt(Transllation.traduzir(txtConclusao.getText()));
            //
            ConexaoMySQL conexaoMySQL = new ConexaoMySQL(conexao);
            if(conexaoMySQL.Conectar()){
                if(conexaoMySQL.Insert(artigos)){
                    JOptionPane.showMessageDialog(null, "Dados gravados com sucesso!");
                    limparTela();
                }
            }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, "Erro no processo de gravação: " + ex.getMessage(), "Erro ao gravar dados no banco de dados.", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

